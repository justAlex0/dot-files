local function button(shortcut, text, on_press)
    return {
        type = "button",
        val = text,
        on_press = function()
            return vim.cmd(on_press)
        end,
        opts = {
            position = "center",
            shortcut = shortcut,
            align_shortcut = "right",
            hl_shortcut = "Keyword",
            cursor = 0,
            width = 60,
            spacing = 1,
        },
    }
end

local M = { "goolord/alpha-nvim" }

M.config = function()
    local alpha = require("alpha")
    local header = {
        type = "text",
        opts = { position = "center", hl = "Type" },
        val = textwal,
    }
    local buttons = {
        type = "group",
        opts = { spacing = 1 },
        val = { button("", "> new file", "enew") },
    }
    local quotee = {
        type = "text",
        opts = { position = "center", hl = "Type" },
        val = "Well are those who rise in the early morn, while those late to bed I shall forewarn",
    }

    alpha.setup({
        layout = {
            { type = "padding", val = 2 },
            header,
            { type = "padding", val = 5 },
            buttons,
            quotee,
        },
        opts = { margin = 5 },
    })
end

return M
